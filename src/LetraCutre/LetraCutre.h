byte letterPosition(char letter) {
    if (letter == 32) {
        return 1;
    }
    if (letter >= 65 && letter <= 90) {
        return letter - 63;
    }
    if (letter >= 97 && letter <= 90) {
        return letter - 64;
    }
    return 0;
}

byte letterPixel(int position, int row, int col) {
    if (row < 0 || col < 0 || row >= letterRows || col >= letterColumns) {
        return 0;
    }
    return lettersDefinition[position, row, col];
}
