const byte mask[] = {
    B10000000,
    B01000000,
    B00100000,
    B00010000,
    B00001000,
    B00000100,
    B00000010,
    B00000001
};

byte letterPosition(char letter) {
    if (letter == 32) {
        return 1;
    }
    if (letter == 63) {
        return 0;
    }
    if (letter >= 65 && letter <= 90) {
        return letter - 63;
    }
    if (letter >= 97 && letter <= 90) {
        return letter - 64;
    }
    return 0;
}

byte letterPixel(int position, byte row, byte col) {
    if (row < 0 || col < 0 || row >= letterRows || col >= letterColumns) {
        return 0;
    }
    byte letterRow = lettersDefinition[(position * letterRows) + row];
    byte rowMask = mask[col];
    return (letterRow & rowMask) > 0 ? 1 : 0;
}
